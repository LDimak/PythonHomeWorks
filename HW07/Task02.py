'''
Напишите функцию print_operation_table(operation, num_rows=6, num_columns=6),
которая принимает в качестве аргумента функцию, вычисляющую элемент по номеру строки и столбца.
Аргументы num_rows и num_columns указывают число строк и столбцов таблицы, которые должны быть распечатаны.
Нумерация строк и столбцов идет с единицы (подумайте, почему не с нуля).
Примечание: бинарной операцией называется любая операция, у которой ровно два аргумента,
как, например, у операции умножения.
'''

def print_operation_table(operation, num_rows, num_columns):
    """
    Функция для печати таблицы операций.

    Аргументы:
    operation -- бинарная операция (функция), вычисляющая элемент по номеру строки и столбца.
    num_rows -- число строк в таблице.
    num_columns -- число столбцов в таблице.
    """
    for row in range(1, num_rows + 1):
        for col in range(1, num_columns + 1):
            # Вычисляем элемент по номеру строки и столбца
            value = operation(row, col)
            # Печатаем элемент, добавляя пробел в начало, если число меньше 10
            print(f"{' ' if value < 10 else ''}{value}", end=' ')
        print()  # Переходим на следующую строку таблицы

def multiply(x, y):
    return x * y

print_operation_table(multiply, 10, 10)