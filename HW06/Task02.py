"""
Задача 32: Определить индексы элементов массива (списка), значения которых принадлежат заданному
диапазону (т.е. не меньше заданного минимума и не больше заданного максимума)

"""

# Ввод исходного списка и границ диапазона
list_1 = list(map(int, input("Введите элементы списка через пробел: ").split()))
min_val = int(input("Введите минимальное значение диапазона: "))
max_val = int(input("Введите максимальное значение диапазона: "))

# Создание списка для хранения индексов элементов, удовлетворяющих условию
inside_interval = []

# Поиск индексов элементов, удовлетворяющих условию
for i in range(len(list_1)):
    if min_val <= list_1[i] <= max_val:
        inside_interval.append(i)

# Вывод списка индексов
print("Индексы элементов, удовлетворяющих условию:")
print(inside_interval)




